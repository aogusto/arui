function ContextMenuContent({
  className,
  children,
  ...props
}: React.ComponentProps<typeof ContextMenuPrimitive.Content> & {
  side?: "top" | "right" | "bottom" | "left"
}) {
  // Pattern B: o pill segue o item destacado (Radix marca com [data-highlighted]
  // em runtime, hover ou teclado). Uma instância por superfície independente.
  // Content é gated por Presence (Radix): o nó real só existe algum tempo
  // depois do primeiro commit. O callback ref do hook resolve isso na fonte:
  // quando o Radix monta o Content, o ref dispara, o efeito re-roda e mede.
  const { ref: pillRef, geometry } = useGlassHighlight({
    activeSelector: "[data-highlighted]",
  })
  return (
    <ContextMenuPrimitive.Portal>
      <ContextMenuPrimitive.Content
        ref={pillRef}
        data-slot="context-menu-content"
        className={cn("relative z-50 max-h-(--radix-context-menu-content-available-height) min-w-36 origin-(--radix-context-menu-content-transform-origin) overflow-x-hidden overflow-y-auto rounded-md p-1 text-popover-foreground duration-100 data-[side=bottom]:slide-in-from-top-2 data-[side=left]:slide-in-from-right-2 data-[side=right]:slide-in-from-left-2 data-[side=top]:slide-in-from-bottom-2 data-[state=open]:animate-in data-[state=open]:fade-in-0 data-[state=open]:zoom-in-95 data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=closed]:zoom-out-95 **:data-[slot$=-separator]:bg-foreground/5", glass.regular, className )}
        {...props}
      >
        <GlassPill geometry={geometry} />
        {children}
      </ContextMenuPrimitive.Content>
    </ContextMenuPrimitive.Portal>
  )
}

